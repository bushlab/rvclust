#' RVCLUST is a modular framework for aggregating rare event features. \cr
#' These features encode events whose low frequency limits our ability \cr
#' to detect their effects with statistical significance. \cr
#' \cr
#' Annotation-based clustering attempts to identify groups of features \cr
#' likely to exhibit a similar effect on an outcome. These groups are \cr
#' the collapsed to generate aggregate features with a higher frequency \cr
#' of variation than their constituent members. \cr
#' ---------------------------------------------------------------------- '#\cr
